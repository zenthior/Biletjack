// Smooth scroll navigation
document.querySelectorAll('.privacy-navigation .nav-item').forEach(item => {
    item.addEventListener('click', function() {
        const section = this.getAttribute('data-section');
        const target = document.getElementById(section);
        if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
            
            // Update active state
            document.querySelectorAll('.privacy-navigation .nav-item').forEach(nav => nav.classList.remove('active'));
            this.classList.add('active');
        }
    });
});

// Scroll spy
window.addEventListener('scroll', function() {
    const sections = document.querySelectorAll('.content-section[id]');
    const navItems = document.querySelectorAll('.privacy-navigation .nav-item');
    
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.clientHeight;
        if (scrollY >= (sectionTop - 200)) {
            current = section.getAttribute('id');
        }
    });
    
    navItems.forEach(item => {
        item.classList.remove('active');
        if (item.getAttribute('data-section') === current) {
            item.classList.add('active');
        }
    });
});
</script>

